def tik(x):
	for i in x + "\n":
		sys.stdout.write(i)
		sys.stdout.flush()
		time.sleep(0.025)

def tiks(x):
        for i in x + "\n":
                sys.stdout.write(i)
                sys.stdout.flush()
                time.sleep(0.0025)


